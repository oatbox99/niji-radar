# 季節（月別カレンダー：解禁/禁漁/冬期C&R/雪代/産卵）
# --------------------------------------------------------------------------- #
def season_note(today: Optional[str] = None) -> Optional[Dict[str, str]]:
    """群馬の河川ニジマスの季節メモ。エビデンス: 解禁3/1・禁漁9/21〜2月末、冬期C&R例外、雪代4-5月。

    正確な解禁/禁漁/営業日は漁協・年で異なるため、月単位の粗い一般則として扱う。
    """
    d = dt.date.fromisoformat(today) if today else dt.date.today()
    m = d.month
    if m in (10, 11, 12, 1, 2):
        return {"level": "info",
                "msg": "冬期ニジマス釣場のシーズンです（一般渓流は禁漁）。水温が好適域に戻り"
                       "活性・C&R安全性とも良好。放流直後が特に狙い目です。正確な営業期間は各釣場でご確認を。"}
    if m == 3:
        return {"level": "info",
                "msg": "一般渓流の解禁期（概ね3/1）ですが、水温が低く不安定で活性はムラがあります。"
                       "低標高の里川が先行。日中の最暖時が勝負です。"}
    if m in (4, 5):
        return {"level": "info",
                "msg": "里川は盛期に向かいますが、山岳本流（利根川など）は雪代（雪解けの白濁り・低温）で"
                       "食い渋る時期です。雪代の川は午前優先。低標高・支流ほど影響は小さめです。"}
    if m == 6:
        return {"level": "info",
                "msg": "雪代明けの本流大型が最盛期になりやすい好季です。ただし6月下旬に水温が急上昇すると"
                       "低標高の本流は失速します。"}
    if m in (7, 8):
        return {"level": "warn",
                "msg": "盛夏の高水温期です。低標高はニジマスに厳しく（20℃超で摂餌停止・弱る）、"
                       "標高の高い上流・源流や、朝夕の涼しい時間、降雨後の水温低下が狙い目です。"}
    # 9月
    return {"level": "warn",
            "msg": "一般渓流は9/20頃までで、以降は禁漁期に入ります（産卵親魚の保護）。"
                   "正確な期日は各漁協でご確認ください。"}


def method_label(methods: List[str], catch_release: bool) -> str:
    """使用可の釣法とC&R区分の1行表示。"""
    m = "・".join(methods) if methods else "—"
    cr = "全キャッチ&リリース" if catch_release else "一般（キープ可・要遊漁券）"
    return f"{m}／{cr}"


