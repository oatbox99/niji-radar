def lake_depth_note(surf: Optional[float]) -> str:
    """表層水温から狙うべき深度レンジ/手法の目安（推定・断定しない）。"""
    if surf is None:
        return "表層水温が推定できません。現地で水温を確かめ、表層→中層→深場と刻んで探ってください。"
    if surf < 5:
        return "低水温期。魚は動きが鈍く、浅場〜中層をスローに。アイスアウト前後は浅場(〜3m)に集中する好機も。"
    if surf <= 18:
        return "表層が適水温。浅場〜表層(〜3m)でショアからも狙えます。ミノー・スプーン・フライの表層〜中層。"
    if surf <= 20:
        return "表層はやや高め。中層を刻んでレンジを探ってください。朝夕まづめが有利です。"
    return ("表層が高水温で魚は深場へ。ボート/トローリング/ジギングで躍層直上の冷水層を。"
            "岸釣りなら朝夕まづめの深い岸・流れ込み(冷水)に絞りを。")


def lake_season_note(today: Optional[str] = None) -> Optional[Dict[str, str]]:
    """群馬の湖ニジマスの季節メモ。エビデンス: アイスアウト/ターンオーバー窓・高標高湖の結氷。"""
    d = dt.date.fromisoformat(today) if today else dt.date.today()
    m = d.month
    if m in (4, 5):
        return {"level": "info",
                "msg": "アイスアウト・春循環の時期です。氷解・雪解け直後は浅場(〜3m)に魚が集中し、"
                       "年間屈指の好機。ただし高標高湖は道路開通(金精峠は概ね4月下旬)が前提です。"}
    if m == 6:
        return {"level": "info",
                "msg": "春本番。表層が適水温で浅場・ショアが狙いやすい時期です。C&R水温リスクも低めです。"}
    if m in (7, 8, 9):
        return {"level": "warn",
                "msg": "盛夏・成層期。表層が高温化し魚は躍層直上の中層〜深場へ。低標高の湖は表層が高すぎ"
                       "リリースも厳しくなります。高標高湖・朝夕まづめ・深度探索が鍵です。"}
    if m in (10, 11):
        return {"level": "info",
                "msg": "秋のターンオーバー。全層が混ざり魚が浅場に戻る好機です。高標高湖は11月で"
                       "閉鎖・結氷に向かうため、営業期間を必ずご確認ください。"}
    return {"level": "warn",
            "msg": "厳寒・端境期。高標高のニジマス湖は結氷・道路冬季閉鎖の時期です。"
                   "結氷する平地湖はワカサギ氷上釣りの領域で、ニジマスの対象外です。要現地確認。"}
