def fishing_tips(
    level: str,
    quality: Optional[str],
    turbidity: Optional[int],
    water_trend: Optional[str],
    methods: Optional[List[str]] = None,
    days_since_stock: Optional[int] = None,
    water_temp: Optional[float] = None,
) -> List[str]:
    """判定・活性・濁り・水位トレンド・使用可の釣法・放流経過・実水温から狙い方を返す（純関数）。

    活性の良し悪しは quality で、狙う『レンジ（層）』は実水温で分岐する — 冷水では活性が
    高くても魚は底に定位するため（<10℃=ボトム/中層、10℃超=表層可、ドライ成立≒10℃/50°F）、
    quality だけで表層/ドライを勧めると冷水期に誤指南になる。
    """
    tips: List[str] = []
    methods = methods or ["ルアー", "フライ"]
    has = lambda m: m in methods  # noqa: E731
    # 低水温は活性が高くても魚は底に定位。quality=低活性 は classify_quality 上つねに<10℃由来
    # なので、実水温が渡らなくても冷水扱いにする。
    cold = (water_temp is not None and water_temp < 10.0) or quality == "低活性"

    if level == "NO_GO":
        tips.append("今日は竿を出せる状態ではありません。水位・濁り・水温が落ち着くのを待ちましょう。"
                    "増水が引いて笹濁りが残る『引き際』（雨後半日〜2日）は年に数度の大チャンスです。")
        return tips

    if quality in ("高水温減退", "高水温危険"):
        tips.append("水温が高めです。日中は物陰・深場・流れ込み（涼しい水）を狙い、"
                    "朝夕の涼しい時間に集中してください。C&Rでは魚を長く水から出さず、素早くリリースを。")
    elif quality in ("絶好", "好適") and not cold:
        parts = ["活性が高い水温帯です。"]
        if has("ルアー"):
            parts.append("ルアーは巻きの釣り（スプーン・ミノー・クランク）で流心や瀬を広く探れます。")
        if has("フライ"):
            parts.append("フライは中〜表層。ライズがあればドライ、無ければニンフで通しましょう。")
        if has("エサ"):
            parts.append("エサ（ミミズ・ブドウ虫・イクラ）は流れに乗せてナチュラルドリフトで。")
        tips.append("".join(parts))
    elif cold:
        # 低水温は活性が高くても魚は底に定位 → レンジはボトム/中層・スロー。
        head = ("活性はありますが冷水です。" if quality in ("絶好", "好適")
                else "低水温で動きが鈍い日です。")
        parts = [head + "ボトム〜中層をスローに。"]
        if has("ルアー"):
            parts.append("ルアーは小型・ゆっくり、ボトムノック気味に。")
        if has("フライ"):
            parts.append("フライはミッジ（#18-24）やエッグ、重めのニンフを底付近で。")
        if has("エサ"):
            parts.append("エサは軽いオモリで底をゆっくり転がすイメージで。")
        tips.append("".join(parts))
    else:
        tips.append("コンディションが読みにくい日です。実績ポイントでレンジ・カラー・スピードを"
                    "変えながら反応を探り、水色と水温をご自身の目で確かめてください。")

    if turbidity == 1:
        tips.append("笹濁りはニジマスの警戒心が薄れ、むしろチャンスです。"
                    + ("ルアーはアピール強め（ゴールド/チャート）で目立たせて。" if has("ルアー") else "")
                    + ("フライは少し大きめ・シルエットの出るパターンを。" if has("フライ") else ""))
    elif turbidity == 0:
        tips.append("水が澄んでいる日は警戒心が高めです。ラインを細く、ルアー/フライも"
                    "ナチュラル系に落とし、物陰や深場を静かに攻めましょう。")

    # 放流ブーストは「荒食いできる状態」= 好適以上・非高水温のときだけ勧める（高水温減退時に
    # 『荒食いの大チャンス』と煽ると保全メッセージと矛盾するため）。
    if days_since_stock is not None and days_since_stock <= 3 \
            and quality in ("絶好", "好適"):
        tips.append(f"{days_since_stock}日前に放流されたばかりです。放流直後は派手なカラーの"
                    "スプーン速巻きに好反応。ポイントに散る前が勝負です。")
    elif days_since_stock is not None and days_since_stock > 14:
        tips.append(f"前回放流から{days_since_stock}日でスレが進んでいます。地味なカラー・"
                    "スロー・レンジ変更で口を使わせる工夫を。追加放流の情報も要チェックです。")

    if water_trend == "上昇":
        tips.append("水位が上昇中です。安全第一で、無理な立ち込みは避けてください。"
                    "急な増水を感じたらすぐ撤退を。")
    return tips


def cr_note(cr_risk: Optional[str], catch_release: bool) -> Optional[str]:
    """C&R区間での保全メッセージ（水温連動）。"""
    if not catch_release:
        return None
    if cr_risk == "nogo":
        return ("水温が高く、リリースした魚が死ぬリスクが高い日です。"
                "魚の保全のため、この区間では見送りをおすすめします。")
    if cr_risk == "strong":
        return ("水温がやや高めです。釣った魚は水から出す時間を最小にし、"
                "手早くリリースしてください（できれば魚を水中で外す）。")
    if cr_risk == "caution":
        return "水温が上がり始めています。魚の消耗に配慮し、丁寧なリリースを心がけてください。"
    return None


