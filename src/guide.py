_WD = ["月", "火", "水", "木", "金", "土", "日"]


def jp_date(iso: Optional[str], with_year: bool = False) -> str:
    """ISO日付を『2月18日(土)』/『2026年2月18日(土)』の和文表記に。不正値はそのまま返す。"""
    if not iso:
        return "—"
    try:
        d = dt.date.fromisoformat(iso[:10])
    except ValueError:
        return iso
    wd = _WD[d.weekday()]
    return f"{d.year}年{d.month}月{d.day}日({wd})" if with_year else f"{d.month}月{d.day}日({wd})"


def freshness(as_of: Optional[str], today: Optional[str] = None) -> Dict[str, Any]:
    """基準日(最新の実データ日)が今日からどれだけ古いか。表示用ラベル+レベル。"""
    t = dt.date.fromisoformat(today) if today else dt.date.today()
    if not as_of:
        return {"days": None, "label": "基準日不明", "level": "warn"}
    try:
        d = dt.date.fromisoformat(as_of[:10])
    except ValueError:
        return {"days": None, "label": "基準日不明", "level": "warn"}
    n = (t - d).days
    if n <= 0:
        return {"days": 0, "label": "最新（本日反映）", "level": "ok"}
    if n == 1:
        return {"days": 1, "label": "前日まで反映", "level": "ok"}
    if n <= 3:
        return {"days": n, "label": f"{n}日前まで反映", "level": "ok"}
    return {"days": n, "label": f"{n}日前（やや古い・更新推奨）", "level": "warn"}


